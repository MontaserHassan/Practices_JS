/////////////////////////////////////// while ///////////////////////////////////////


let i = 0;
while (i < 5) {
    console.log(`Iteration ${i + 1}`);
    i++;
};


/////////////////////////////////////// do while ///////////////////////////////////////


let j = 0;
do {
    console.log(`Iteration ${j + 1}`);
    j++;
} while (j < 5);


/////////////////////////////////////// for ///////////////////////////////////////


for (let i = 0; i < 5; i++) {
    console.log(`Iteration ${i + 1}`);
};


/////////////////////////////////////// for in {object} ///////////////////////////////////////


const object = { a: 1, b: 2, c: 3 };
for (const property in object) {
    console.log(`${property}: ${object[property]}`);
};


/////////////////////////////////////// for of [array] ///////////////////////////////////////


const array1 = ['a', 'b', 'c'];
for (const element of array1) {
    console.log(element);
};