/////////////////////////////////////// while ///////////////////////////////////////


let i = 0;

while (i < 5) {
    console.log(`Iteration ${i + 1}`);
    i++;
};


/////////////////////////////////////// do while ///////////////////////////////////////


let j = 0;

do {
    console.log(`Iteration ${j + 1}`);
    j++;
} while (j < 5);


/////////////////////////////////////// for ///////////////////////////////////////


for (let i = 0; i < 5; i++) {
    console.log(`Iteration ${i + 1}`);
};